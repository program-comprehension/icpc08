<a href="docs/Kothari.pdf">The paper can be downloaded here</a> <BR><BR>
<H3>Bio</h3> 

Dr. Kothari leads the Software Systems Group in the Electrical and Computer Engineering department at Iowa State University. He has pioneered the Knowledge-Centric Software (KCS) technology for building automation tools for maintenance and evolution of software. He founded EnSoft in May 2002 to commercialize the KCS technology. EnSoft sells software tools and provides tool-based services for software developed in ADA, C, C++, COBOL, Java, Simulink, and SQL. EnSoft focuses on three market segments: control systems software, financial services software, and systems software for networking and telecommunications. EnSoft&#8217;s SimDiff product is used by more than 50 major corporations in US, Europe, and Japan.


