<H2>Terence Parr</H2>
<h2 class="red" style="font-size:15px;">"The
 Reuse of Grammars with Embedded Semantic Actions"</h2>
<img src="http://www.cs.usfca.edu/~parrt/australia-4.jpg" style="height:100px;float:right;padding:10px;">
Assistant professor and graduate program director<BR>
Computer Science<BR>
University of San Francisco<BR>
<a href="http://www.cs.usfca.edu/~parrt/" 
target="_blank">http://www.cs.usfca.edu/~parrt/</a>

<H3>Abstract</h3>
 Formal grammars are at the heart of many important applications,
 including source code rewriting systems and program comprehension
 tools. To go beyond syntax checking, grammars must have some way to
 specify the translation or interpretation logic (the semantics).
 Unfortunately, the act of specifying the semantics can lock a grammar
into one specific application since the grammar is often modified to
suit.  Reusing this grammar then becomes a serious problem even though
 it might be needed verbatim in another application but with different
 semantics.
<BR><BR>
  Researchers have explored a number of formal strategies for decoupling
 the syntax and semantics specifications (e.g., SDF+ASF, Stratego, and
  friends).  Once grammars are decoupled from the semantics, programmers
  can treat grammars just like libraries.  In contrast, ANTLR cannot
  enforce this separation.  An ANTLR grammar's syntax and semantics are
  often entangled because programmers embed arbitrary actions within the
  rules.
<BR><BR>
  In this talk, I will summarize and illustrate solutions for the most
  common grammar reuse problems encountered by ANTLR users: grammar
  composition and grammars derived from prototypes.  ANTLR v3.1
  incorporates a delegation model for composing grammars and my proposed
  "lowbrow" solution for dealing with prototypical grammars may surprise
  you.
<BR><BR>


