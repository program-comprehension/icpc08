<H2>Suraj C. Kothari </h2>



<h2 class="red" style="font-size:15px;">"Scalable Program Comprehension for Analyzing Complex 
Defects"</h2>
<img src="http://www.ece.iastate.edu/typo3temp/pics/271de956c8.gif" style="height:100px;float:right;padding:10px;">
Professor and Chair, Software Systems Group in the Electrical and Computer Engineering Department, Iowa State University (ISU), USA<BR>
&<BR>
President of EnSoft Corp., USA<BR>
Email: kothari@iastate.edu



<H3>Abstract</H3>
It is no longer the time and space complexity, but it is the human complexity . programmer.s 
time and effort, that is really the concern and the bottleneck for achieving high software 
productivity and quality. Effective program comprehension tools are needed to reduce the human 
complexity.
<BR><BR>
This talk will be about an interactive Query-Model-Refine (QMR) approach for building program 
comprehension tools. The QMR approach has been used for building a number of program 
comprehension tools for different applications: interactive automatic parallelization, business 
rule analysis, auditing safety-critical control software, and defect analysis. This presentation 
will be accompanied by a demonstration of a program comprehension tool called Atlas. We will 
show its use for analyzing complex defects in an operating systems code. The demonstration will 
illustrate how the tedious and time-consuming task of analyzing complex defects can be greatly 
simplified so that instead of hours we can do the analysis in minutes and avoid human errors 
that are likely to occur without the tool.
<BR><BR>
The QMR is an extensible approach. The query language, the models, and the refinement 
transformations will evolve as we get better insights into solving different types of program 
comprehension problems. 
