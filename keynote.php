<a href="docs/Parr.pdf">The paper can be downloaded here</a> <BR><BR>


<H3>Bio</h3>
  Terence Parr is a professor of computer science at the University of
  San Francisco where he continues to work on his ANTLR parser
  generator, http://www.antlr.org. Terence recently returned from years
  in industry where he co-founded jGuru.com. He herded programmers and
