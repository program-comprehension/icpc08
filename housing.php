<h2>Public Transport</h2>
<img src="http://upload.wikimedia.org/wikipedia/commons/thumb/3/37/NS_Kaartautomaten.jpg/180px-NS_Kaartautomaten.jpg" style="float:right;margin:10px;" height=175px>
Buy your train tickets, before entering the train, at a yellow vending machine at the airport. There are many of these machines, the usually accept credit cards. 
It costs EUR 2.60 to get into Amsterdam. Way below the average taxi cost. <BR><BR>
If you get on a train at the airport make sure you are in the correct one. Some trains travel to Amsterdam Central Station (CS), the conference venue is near Amsterdam CS, some trainstravel to Amsterdam Zuid WTC/Amsterdam RAI, these are 2 different directions. If you do get on the wrong train, take a metro (no. 51) from Zuid WTC or RAI to Amsterdam CS or vice versa.
<BR>
<a href="http://www.ns.nl/servlet/Satellite?cid=1075985690180&pagename=www.ns.nl%2FPage%2FSuperHomepageEnglish&lang=en&c=Page" target="_blank">Train schedules (in English)</a><BR>
<BR><BR>

For the metro/tram/bus, there are different possiblities. 
You can buy a multiple day pass at a "GVB" vending machine at every metro station. This is probably the most convenient, but more expensive. There are oneway tickets, 24 hour, 48 hour or 72 hour or even 96 hour tickets available in the machines.
<BR><BR>
 24 hours &nbsp;&nbsp;	&#8364; 7,00<BR>
 48 hours &nbsp;&nbsp;	 &#8364; 11,50<BR>
 72 hours 	 &nbsp;&nbsp; &#8364; 14,50<BR>
 96 hours &nbsp;&nbsp;	 &#8364; 17,50<BR>
<BR><BR>
The cheapest way to travel by bus/tram and metro in the Netherlands would be to buy a "strippenkaart", also available in the GVB vending machines. 
<img src="http://www.gvb.nl/reizigers/touristguide/PublishingImages/strippenkaarten2007.jpg" width=100px style="float:right;margin:5px;">
If you buy a 15-strippenkaart, it costs 6.90. Every time you go by tram/bus/metro you have to stamp the card, the amount of zones you travel plus 1. For example from RAI station to Nieuwmarkt, it is 2 zones, so you stamp at number 3, the next time you travel the same distance at 6, etc.  
Since there are 15 stamp possibilities ("strippen") you can use the 15 strippenkaart for 5 trips travelling 2 zones every time. 
There also exists a 45 strippenkaart, &#8364; 20.40, if you intend to travel a lot by tram/bus or metro. <BR><BR>


