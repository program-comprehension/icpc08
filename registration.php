<h2>Disclaimer</H2>
The registration for ICPC 2008 is open. 
<BR><BR>
ICPC 2008 registration is handled by VU University Amsterdam. <b>Please read the following carefully.</b>

<H2>Early registration discount</H2>
The registration payment must be received by <B>May 10</B> to qualify for early registration discount.
<BR>


<H2>Payment methods</h2>

<H4>Credit cards</h4>
Your credit card charge will be processed by VU University Amsterdam (Vrije Universiteit Amsterdam). 
By providing information for a credit card payment, you agree that VU University Amsterdam may act as your agent in the placement of the credit card charge. For security, the credit card charge is processed off-line within a few days after the on-line registration request is submitted. 
<BR><BR>
If, for any reason, a credit card charge cannot be placed in euros (EUR), it will be placed in US Dollars (USD) at the equivalent USD/EUR conversion rate on the day the charge is placed by VU University Amsterdam. 
<BR><BR>
Credit card processing requires the <B>Card Verification Code (CVC)</b> that is printed on your card. For Visa and Mastercard this CVC number is printed on the back of the card in the signature area. For Amex this is printed on the front of the card. These numbers are flat printing, not raised lettering. 
<BR><BR>
Please advise whoever will review your charge statement that this item is your conference registration. Unnecessary disputes on credit card charges cause higher fees and therefore higher registration rates. 

<H4>Bank transfers</h4>
If you need to pay for your conference registration by bank transfer you will find all details on the registration pages. 
Registrations indicating payment by bank transfer are not final until payment is received in full. 
<BR><BR>
Questions can be send to: <a href=mailto:icpc@cs.vu.nl>icpc@cs.vu.nl</a>


<H2>Important notes</h2>
As a technical meeting of the field and not being a trade show, all paper presenters need to register for the conference. 
<BR><BR>
Student rates applies only to <B>full-time</B> undergraduate and graduate students, not otherwise employed. Proof of full-time student status, which can be a letter from your Department Chair or Thesis advisor on university stationery, is required on request by the conference.
<BR><BR>
Registration fees for a full conference includes free workshop attendance, one copy of conference materials, e.g. proceedings, one ticket to the conference reception and one ticket to the conference dinner . These items are not seperately priced. Registration fees include any applicable taxes.  
<BR><BR>
Cancellations are subject to an administration fee of EUR 100. No refunds after May 10. 
<BR><BR>
Any substitution of attendees must be approved in advance by the General Chair. 
<BR><BR>
IEEE members qualify for reduced conference fares, see the table below.

