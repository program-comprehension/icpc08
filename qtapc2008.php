<h3>Call for papers:</h3>

  Developers need to understand the system they work on in order to
  improve quality, enhance functionality, and re-engineer for new
  deployment environments. Querying tools are designed to help developers
  comprehend the varied and complex relationships within a codebase. They
  enable the interactive exploration of a system and the definition of
  custom views. Recently, code queries have proved useful in many
  development tasks, including detecting architectural flaws, identifying
  refactoring opportunities, finding crosscutting concerns, locating bugs
  and enforcing coding conventions that are specific to an API. Code
  queries are also vital in a diagnostic role, such as for monitoring
  development progress, assessing design quality, tracking unwanted
  dependencies, and gathering quality metrics.
<BR><BR>

  This working session aims to bring together researchers and
  practitioners to explore the state of the art in code query
  technologies, and discover new ways in which these technologies may be
  useful in program comprehension.
<BR><BR>
  We welcome contributions on the following topics:
<ul>   <li> Code query languages
   <li> Query evaluation mechanisms
   <li> Artifacts models
   <li> Tool integration
   <li> Tool benchmarking
   <li> Queries for quality assurance
   <li> Queries for monitoring development
   <li> Queries and software visualization
   <li> Queries in reverse engineering
   <li> Queries on software archives
   <li> Queries for documentation purposes
   <li> Any other (new) application
</ul>

  Three kinds of submissions are solicited:

<ol type="i">
  <li>  position statements,
   <li> tool demos, and
   <li> proposals for comparative and empirical studies.
</ol>

  Submissions should not exceed four pages.  A subset of the submissions
  will be selected for presentation at the working session. A final
  post-conference document with all contributions will be published.
<BR><BR>
  The submission deadline is <b>May 14, 2008</b>.
<BR><BR>
  Please submit a PostScript or PDF version of your short position paper
  (in the <a href="http://www.computer.org/portal/pages/cscps/cps/cps_forms.html" target="_blank">IEEE CS proceedings style, two-column format</a>) by email to one of
  the organizers:
