<H1 class="workshop" id="IRPC">Working Session on Industrial Realities of Program Comprehension (IRPC 2008) </H1>

<h2>Objective</h2>

The aim of this working session is to foster discussion among researchers and practitioners about the realities regarding industrial adoption of program comprehension techniques. These realities are of various kinds, including technological challenges, economic viability, business risks, and social obstacles.

Topic for discussion may include:
<ul>
	<li>experiences with successful and unsuccessful industrial application,
	<li>challenges encountered when attempting industrial application of program comprehension techniques, 
<li>	strategies for dealing with these challenges, 
	<li>successful and sustainable business models for program comprehension
<li>	how to convey benefits of program comprehension techniques to management
<li>	how to minimize business risks of program comprehension techniques
<li>	how to package program comprehension techniques for particular audiences and/or markets
</ul>
<h2>Intended Audience</h2>

Researchers and practitioners that have experience with the (attempted) application of program comprehension techniques in industrial settings.

<h2>Format</h2>

The working session mainly works by position statements and/or brief experience reports, and focuses on discussion rather than full presentations. The position statements as well as a summary of the discussion will be published on a designated web site.


<h2>More information and website</h2>
<a href="http://www.sig.nl/irpc2008/" target="_blank">Please refer to the IRPC website</a>

